package business;

public class PythagoreanTripletBusiness {

    public int potentiation(int value){
        return value * value;
    }

    public int sumOfAllValues(int a, int b, int c){
        return a + b + c;
    }

    public int calculatePythagoreanTripletValue(int a, int b, int c) {
        return a * b * c;
    }

}
